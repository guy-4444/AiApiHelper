package org.guy.library

import org.guy.library.clients.AiClient
import org.guy.library.clients.GeminiClient
import org.guy.library.clients.OpenAiClient
import org.guy.library.models.AiModel

/**
 * The main entry point for the Simple AI Library.
 * Instantiate this class with your API key and chosen model to start asking questions.
 */
class SimpleAi(
    private val apiKey: String,
    private val model: AiModel
) {
    private val client: AiClient = when {
        model.isGemini() -> GeminiClient(apiKey, model)
        model.isOpenAi() || model.isGrok() -> OpenAiClient(apiKey, model)
        else -> throw IllegalArgumentException("Unsupported AI model: $model")
    }

    /**
     * If true, the library will automatically calculate and print the estimated API cost
     * (based on input and output tokens) to the console after every call.
     */
    var logCost: Boolean = false

    init {
        client.onTokenUsage = { inputTokens, outputTokens ->
            if (logCost) {
                val inputCost = (inputTokens.toDouble() / 1_000_000) * model.inputCostPerMillion
                val outputCost = (outputTokens.toDouble() / 1_000_000) * model.outputCostPerMillion
                val totalCost = inputCost + outputCost
                
                println("\n[AiApi Cost Logger]")
                println("Model: ${model.modelName}")
                println("Tokens: $inputTokens in | $outputTokens out")
                println("Est. Cost: $${String.format("%.6f", totalCost)}")
                println("-------------------\n")
            }
        }
    }

    /**
     * Sends a prompt to the AI model and returns its text response.
     * @param prompt The prompt to ask the AI.
     * @return The text response generated by the AI.
     */
    fun ask(prompt: String): String {
        return client.generate(prompt)
    }

    /**
     * Sends a prompt and requests the AI to return data that matches the structure of [type].
     * @param prompt The prompt to ask the AI.
     * @param type The Java Type to deserialize the AI's response into.
     * @return An instance of [T] parsed from the AI response.
     */
    fun <T> askForType(prompt: String, type: java.lang.reflect.Type): T {
        return client.generateObject(prompt, type)
    }

    /**
     * Sends a prompt and requests the AI to return data that matches the structure of [T].
     * @param prompt The prompt to ask the AI.
     * @return An instance of [T] parsed from the AI response.
     */
    inline fun <reified T> askForType(prompt: String): T {
        val type = object : com.google.gson.reflect.TypeToken<T>() {}.type
        return askForType<T>(prompt, type)
    }
}
